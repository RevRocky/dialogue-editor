import { useCallback } from 'react';
import {
  ReactFlow,
  Background,
  Controls,
  MiniMap,
  addEdge,
  useNodesState,
  useEdgesState,
  type OnConnect,
} from '@xyflow/react';

import '@xyflow/react/dist/style.css';

import { INITIAL_NODES, nodeTypes, TEST_NODES } from './nodes';
import { INITIAL_EDGES, TEST_EDGES, edgeTypes } from './edges';

export default function App() {
  const [nodes, , onNodesChange] = useNodesState(TEST_NODES);
  const [edges, setEdges, onEdgesChange] = useEdgesState(TEST_EDGES);
  const onConnect: OnConnect = useCallback(
    (connection) => setEdges((edges) => addEdge(connection, edges)),
    [setEdges]
  );

  return (
    <ReactFlow
      nodes={nodes}
      nodeTypes={nodeTypes}
      onNodesChange={onNodesChange}
      edges={edges}
      edgeTypes={edgeTypes}
      onEdgesChange={onEdgesChange}
      onConnect={onConnect}
      fitView
    >
      <Background />
      <MiniMap />
      <Controls />
    </ReactFlow>
  );
}
